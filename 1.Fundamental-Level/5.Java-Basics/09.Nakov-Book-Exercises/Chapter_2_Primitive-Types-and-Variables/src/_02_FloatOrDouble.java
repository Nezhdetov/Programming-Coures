
public class _02_FloatOrDouble {
	public static void main(String[] args) {
		double firstDoubleNumber = 34.567839023;
		float firstFloatNumber = 12.345f;
		double secondDoubleNumber = 8923.1234857;
		float secondFloatNumber = 3456.091f;
		
		System.out.println(firstDoubleNumber);
		System.out.println(firstFloatNumber);
		System.out.println(secondDoubleNumber);
		System.out.println(secondFloatNumber);
	}
}
