public class _01_DeclaringVariables {

    public static void main(String[] args) {
        byte firstByteVariable = 97;
        byte secondByteVariable = -115;
        short firstShortVariable = -10000;
        int firstIntVariable = 52130;
        int secondIntVariable = 4825932;

        System.out.println(firstByteVariable);
        System.out.println(secondByteVariable);
        System.out.println(firstShortVariable);
        System.out.println(firstIntVariable);
        System.out.println(secondIntVariable);
    }
}