public class _03_AssignVariables {

    public static void main(String Args[]) {
        String str = "Palo Alto, CA";
        short shortNumber = 32767;
        //short notShortNumber = 32768;
        //short max value is 32767 ->  2^16;
        int intNumber = 2000000000;
        double doubleNumber = 0.1234567891011;
        float floatNumber = 0.5f;
        long longNumber = 919827112351L;
        char charSymbol = 'c';

        System.out.println("String example -> " + str);
        System.out.println("short example -> " + shortNumber);
        System.out.println("int example -> " + intNumber);
        System.out.println("double example -> " + doubleNumber);
        System.out.println("float example -> " + floatNumber);
        System.out.println("long example -> " + longNumber);
        System.out.println("char example -> " + charSymbol);
    }

}
